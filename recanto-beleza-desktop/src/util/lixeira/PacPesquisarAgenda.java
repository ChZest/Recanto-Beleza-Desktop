    private void preencheTabela(List<Cliente> clientes) {
        if (clientes.isEmpty()) {
            try {
                clientes = ClienteDados.consultarTodos();
            } catch (DadosException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(),
                        "Erro", JOptionPane.HEIGHT);
            }
        }
        if (clientes != null) {
            String[] strTVet = new String[]{"CÃ³digo", "Nome", "Telefone"};
            List<Cliente> listaClientes;
            listaClientes = new ArrayList<>();
            String[][] strAMat = new String[listaClientes.size()][3];
            for (int i = 0; i < listaClientes.size(); i++) {
                Cliente c = listaClientes.get(i);
                strAMat[i][0] = c.getCdCliente() + "";
                strAMat[i][1] = c.getNmCliente();
                strAMat[i][2] = c.getTelefone();
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(strAMat, strTVet));
        }
    }

    private void consultar(String nome) {
        try {
            List<Cliente> clientes = ClienteDados.consultarCampos(nome);
            preencheTabela(clientes);
        } catch (DadosException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),
                    "Erro", JOptionPane.HEIGHT);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar consulta por nome do paciente: " + e.getMessage());
        }
    }

